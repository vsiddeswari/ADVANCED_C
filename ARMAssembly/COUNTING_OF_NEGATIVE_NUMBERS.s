
/* Counting of Negative numbers */

.global _start
_start:
	LDR R0,=DATA1
	MOV R1,#0X0
	LDR R2,LENGTH
	CMP R2,#0X0
	BEQ DONE
LOOP: LDR R3,[R0],#0X4
      CMP R3,#0X0
	  BGE LOOPTEST
	  ADD R1,R1,#0X1
LOOPTEST: ADD R0,R0,#0X4
          SUBS R2,R2,#0X1
		  BNE LOOP
DONE: STR R1,RESULT
	  
DATA1: .WORD 0XE5673456,0XF1355565,0X64356436
LENGTH: .WORD 0X3
RESULT: .WORD 0X0
